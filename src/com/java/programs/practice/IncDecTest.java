package com.java.programs.practice;


/* Increment & Decrement Operators Test*/

public class IncDecTest {
	
	public static void main(String[] args) {
		
		int x=1,y=2;
		
		System.out.println("x = "+x);
		
		System.out.println("y = "+y);
		
		System.out.println(++x*3 + " " + x++);
		
		System.out.println("x = "+x);

		System.out.println (++y+" "+y++);
		
		System.out.println("y = "+y);
	}

}
 