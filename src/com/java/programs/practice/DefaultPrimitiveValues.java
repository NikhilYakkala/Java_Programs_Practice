package com.java.programs.practice;

public class DefaultPrimitiveValues {
	
	private byte b;
	
	private short s;
	
	private int i;
	
	private char c;
	
	private long l;
	
	private boolean bo;
	
	private String string;
	
	private double d;

	public static void main(String[] args) {	
	
	DefaultPrimitiveValues dpv = new DefaultPrimitiveValues();
	
	System.out.println("Default Value for byte = "+dpv.b);
	
	System.out.println("Default Value for short = "+dpv.s);
	
	System.out.println("Default Value for int = "+dpv.i);
	
	System.out.println("Default Value for char ="+dpv.c);
	
	System.out.println("Default Value for long = "+dpv.l);
	
	System.out.println("Default Value for boolean = "+dpv.bo);
	
	System.out.println("Default Value for String = "+dpv.string);
	
	System.out.println("Default Value for double = "+dpv.d);
	
	}
	
}
